
// === evalua el valor, como también que sea el mismo tipo de dato

const dia = 2; //0: Domingo

switch ( dia ){

    case 0:
        console.log('Domingo'); 
        break;

    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miercoles');
        break;
    case 4:
        console.log('Jueves');
        break;
    case 5:
        console.log('Viernes');
        break;
    case 6:
        console.log('Sábado');
        default: 
        console.log('No es Lunes, Martes, Miercoles, Jueves, Viernes, Sábado o Domingo');
        
}



